# 读取翻译文件
with open('app/ui/translations.py', 'r', encoding='utf-8') as f:
    lines = f.readlines()

# 找到 TRANSLATIONS 字典的闭合花括号
in_dict = False
brace_count = 0
insert_at = None
for i, line in enumerate(lines):
    if 'TRANSLATIONS: Dict[str, str] = {' in line:
        in_dict = True
        brace_count = 1
        continue
    if in_dict:
        brace_count += line.count('{') - line.count('}')
        if brace_count == 0:
            insert_at = i
            break

if insert_at is None:
    print("ERROR: Could not find end of TRANSLATIONS dict!")
    exit(1)

# 构建插入行
new_lines = [f'    # === 批量新增: 缺失的 help/tooltip 翻译 ({len(T)}条) ===\n']
for eng in sorted(T.keys()):
    chn = T[eng]
    # 转义双引号
    eng_esc = eng.replace('\\', '\\\\')
    chn_esc = chn.replace('\\', '\\\\')
    new_lines.append(f'    "{eng_esc}": "{chn_esc}",\n')

# 插入
result = lines[:insert_at] + new_lines + lines[insert_at:]

with open('app/ui/translations.py', 'w', encoding='utf-8') as f:
    f.writelines(result)

print(f'Success! Inserted {len(T)} translation entries at line {insert_at}')
