def install_translator(app):
    from PySide6 import QtCore, QtWidgets
    
    class Translator(QtCore.QTranslator):
        def translate(self, context, sourceText, *args):
            return translate(sourceText)
    
    class WidgetTranslator(QtCore.QObject):
        def __init__(self, app):
            super().__init__()
            self.app = app
            self._pass_count = 0
            self.setup_tab_callbacks()
            self.dynamic_timer = QtCore.QTimer()
            self.dynamic_timer.timeout.connect(self._translate_all_dynamic)
            self.dynamic_timer.start(400)  # 快速启动扫描
            # 10秒后降速，120秒后停止密集扫描
            QtCore.QTimer.singleShot(10000, lambda: self.dynamic_timer.setInterval(2000))
            QtCore.QTimer.singleShot(120000, lambda: self.dynamic_timer.stop())

        def _translate_all_dynamic(self):
            """全面扫描并翻译所有动态创建的 UI 元素."""
            self._pass_count += 1
            for widget in self.app.topLevelWidgets():
                # 1) 处理 SectionHeaderButton 自定义绘制的标题
                for child in widget.findChildren(QtWidgets.QPushButton):
                    try:
                        if child.metaObject().className() == "SectionHeaderButton":
                            if hasattr(child, "_title") and child._title:
                                t = translate(child._title)
                                if t != child._title:
                                    child._title = t
                                    child.update()
                            continue  # 跳过常规 QPushButton 逻辑
                    except Exception:
                        pass

