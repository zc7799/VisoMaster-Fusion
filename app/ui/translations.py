    class DialogTranslator(QtCore.QObject):
        def __init__(self, app):
            super().__init__()
            self.app = app
            app.installEventFilter(self)
            
        def translate_all_windows(self):
            for widget in self.app.topLevelWidgets():
                if isinstance(widget, QtWidgets.QDialog):
                    self.translate_dialog(widget)
                elif widget.isWindow():
                    self.translate_window_widget(widget)
            
        def eventFilter(self, obj, event):
            if event.type() == event.Type.Show:
                if isinstance(obj, QtWidgets.QDialog):
                    self.translate_dialog(obj)
                elif isinstance(obj, QtWidgets.QWidget) and obj.isWindow():
                    self.translate_window_widget(obj)
            if event.type() == event.Type.PolishRequest:
                if isinstance(obj, QtWidgets.QDialog):
                    self.translate_dialog(obj)
                elif isinstance(obj, QtWidgets.QWidget) and obj.isWindow():
                    self.translate_window_widget(obj)
            return super().eventFilter(obj, event)
        
        def translate_window_widget(self, widget):
            if widget.windowTitle():
                widget.setWindowTitle(translate(widget.windowTitle()))
            for child in widget.findChildren(QtWidgets.QLabel):
                if child.text():
                    child.setText(translate(child.text()))
            for child in widget.findChildren(QtWidgets.QPushButton):
                if child.text():
                    child.setText(translate(child.text()))
            for child in widget.findChildren(QtWidgets.QDialogButtonBox):
                for btn in child.buttons():
                    if btn.text():
                        btn.setText(translate(btn.text()))
        
        def translate_dialog(self, dialog):
            if dialog.windowTitle():
                dialog.setWindowTitle(translate(dialog.windowTitle()))
            for child in dialog.findChildren(QtWidgets.QLabel):
                if child.text() and child.isVisible():
                    child.setText(translate(child.text()))
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
            for child in dialog.findChildren(QtWidgets.QPushButton):
                if child.text() and child.isVisible():
                    child.setText(translate(child.text()))
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
            for child in dialog.findChildren(QtWidgets.QDialogButtonBox):
                for btn in child.buttons():
                    if btn.text():
                        btn.setText(translate(btn.text()))
            for child in dialog.findChildren(QtWidgets.QCheckBox):
                if child.text() and child.isVisible():
                    child.setText(translate(child.text()))
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
            for child in dialog.findChildren(QtWidgets.QGroupBox):
                if child.title():
                    child.setTitle(translate(child.title()))
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
            for child in dialog.findChildren(QtWidgets.QLineEdit):
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
            for child in dialog.findChildren(QtWidgets.QComboBox):
                if child.toolTip():
                    child.setToolTip(translate(child.toolTip()))
    
